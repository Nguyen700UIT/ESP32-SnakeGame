void initBait()
{
    int maxCol = SNAKE_BOARD_WIDTH / SNAKE_BLOCK_SIZE;
    int maxRow = SNAKE_BOARD_HEIGHT / SNAKE_BLOCK_SIZE;
    bool valid = false;

    while(!valid)
    {
        valid = true;
        bait.x = random(0, maxCol) * SNAKE_BLOCK_SIZE;
        bait.y = random(0, maxRow) * SNAKE_BLOCK_SIZE;
        if(headX == bait.x && headY == bait.y)
        {
            valid = false;
            continue;
        }
        for (int i = 0; i < nTail; ++i)
        {
            if(tailX[i] == bait.x && tailY[i] == bait.y)
            {
                valid = false;
                break;
            }
        }
    }
}

// Reset snake to normal state
void resetSnake() {
    headX = 60;
    headY = 30;
    nTail = 1;
    tailX[0] = headX;
    tailY[0] = headY;
}

void readDirection()
{
    // Đọc cờ ngắt và chặn lỗi quay đầu 180 độ
    if (isUp && snakeDir != down) {
        snakeDir = up;
    }
    else if (isDown && snakeDir != up) {
        snakeDir = down;
    }
    else if (isLeft && snakeDir != right) {
        snakeDir = left;
    }
    else if (isRight && snakeDir != left) {
        snakeDir = right;
    }

    isUp = false;
    isDown = false;
    isLeft = false;
    isRight = false;
}

void move()
{
    prevTailX = tailX[nTail-1];
    prevTailY = tailY[nTail-1];
    int prevX = tailX[0];
    int prevY = tailY[0];
    tailX[0] = headX;
    tailY[0] = headY;
    int tempX, tempY;
    for (int i = 1; i < nTail; ++i)
    {
        tempX = tailX[i];
        tempY = tailY[i];
        tailX[i] = prevX;
        tailY[i] = prevY;
        prevX = tempX;
        prevY = tempY;
    }
    switch(snakeDir)
    {
        case up:
            headY-=SNAKE_BLOCK_SIZE;
            break;
        case down:
            headY+=SNAKE_BLOCK_SIZE;
            break;
        case left:
            headX-=SNAKE_BLOCK_SIZE;
            break;
        case right:
            headX+=SNAKE_BLOCK_SIZE;
            break;
    }
    // if (headX < 0) headX = SNAKE_BOARD_WIDTH - SNAKE_BLOCK_SIZE; else if (headX >= SNAKE_BOARD_WIDTH) headX = 0;
    // if (headY < 0) headY = SNAKE_BOARD_HEIGHT - SNAKE_BLOCK_SIZE; else if (headY >= SNAKE_BOARD_HEIGHT) headY = 0;

}

void isGameOver()
{
    if(headX < 0 || headX >= SNAKE_BOARD_WIDTH || headY < 0 || headY >= SNAKE_BOARD_HEIGHT)
       gameOver = true;
    for (int i = 0; i < nTail - 1; ++i)
    {
        if (headX == tailX[i] && headY == tailY[i]) 
        {
            gameOver = true;
            break;
        }
    }
}

bool eat()
{
    if (headX == bait.x && headY == bait.y) 
    { 
        tft_snake.fillRect(bait.x, bait.y, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, GAME_GREEN); //Delete bait
        nTail++;
        initBait();
        return true;
    }
    return false;
}

void drawSnake(bool& ate)
{
    tft_snake.fillRect(prevTailX, prevTailY, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, GAME_GREEN);
    // 1. CHỌN MẢNG ĐẦU RẮN THEO HƯỚNG
    const uint16_t* currentHeadBmp = headRight; // Mặc định nếu mới vào game
    if (snakeDir == up) currentHeadBmp = headUp;
    else if (snakeDir == down) currentHeadBmp = headDown;
    else if (snakeDir == left) currentHeadBmp = headLeft;
    else if (snakeDir == right) currentHeadBmp = headRight;

    tft_snake.setSwapBytes(true);

    // 4. Vẽ thân rắn (Dùng pushImage cho mảng màu)
    // Cú pháp: pushImage(x, y, w, h, mảng);
    tft_snake.pushImage(tailX[0], tailY[0], SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, (const uint16_t*)snakeBody);

    // 5. Vẽ đầu rắn
    tft_snake.pushImage(headX, headY, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, (const uint16_t*)currentHeadBmp);

    tft_snake.setSwapBytes(false); // Tắt sau khi vẽ xong
}


void drawBait()
{
    tft_snake.setSwapBytes(true);
    tft_snake.pushImage(bait.x, bait.y, SNAKE_BLOCK_SIZE, SNAKE_BLOCK_SIZE, (const uint16_t*)baitBmp);
    tft_snake.setSwapBytes(false);;
}
void drawPlayingUI()
{
  // 1. Kẻ vạch phân cách màu trắng
  tft_snake.drawFastHLine(0, GAME_HEIGHT, SCREEN_WIDTH, TFT_WHITE); 
  
  // 2. Tô nền xám cho thanh UI ở đáy
  tft_snake.fillRect(0, GAME_HEIGHT + 1, SCREEN_WIDTH, SCREEN_HEIGHT - GAME_HEIGHT, TFT_DARKGREY); 

  // 3. In chữ TĨNH
  tft_snake.setTextSize(2);
  tft_snake.setTextColor(TFT_WHITE); // Chữ trắng nền trong suốt
  
  tft_snake.setCursor(10, GAME_HEIGHT + 12);
  tft_snake.print("SCORE: ");
  
  tft_snake.setCursor(180, GAME_HEIGHT + 12);
  tft_snake.print("BEST: ");

  // Gọi lần đầu để in luôn số điểm đang là 0
  drawScore();
}

void drawScore()
{
    tft_snake.setTextSize(2);
    tft_snake.setTextColor(TFT_WHITE, TFT_DARKGREY); 
    
    // Dịch tọa độ X ra sau chữ "SCORE: " (khoảng 85px)
    tft_snake.setCursor(85, GAME_HEIGHT + 12);
    tft_snake.printf("%05d", score); // In 3 chữ số (VD: 005)
    
    // Dịch tọa độ X ra sau chữ "BEST: "
    tft_snake.setCursor(245, GAME_HEIGHT + 12);
    tft_snake.printf("%05d", bestScore);
}

void renderBoard(bool &ate)
{
    drawSnake(ate);
    if(ate) 
        
        drawBait();
}

void drawGameOverUI()
{
    if(!drewGameOverUI)
    {
        tft_snake.setTextSize(2); 
        tft_snake.setTextColor(TFT_WHITE);
        
        // Căn giữa dựa theo GAME_HEIGHT thay vì SCREEN_HEIGHT
        tft_snake.setCursor(SCREEN_WIDTH/2 - 55, GAME_HEIGHT/2 - 10);
        tft_snake.println("GAME OVER");
        drewGameOverUI = true;
    }
}

void gameReset()
{
    score = 0;
    prevScore = -1; // Ép vẽ lại điểm số lần đầu
    resetSnake();
    snakeDir = right;
    gameOver = false;
    
    tft_snake.fillRect(0, 0, SCREEN_WIDTH, GAME_HEIGHT, GAME_GREEN); 
    drewGameOverUI = false;
    drawPlayingUI(); // Khởi tạo lại khung UI tĩnh
    initBait();
    drawBait();
}